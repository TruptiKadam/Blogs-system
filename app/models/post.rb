#===========================================================================
# POST
#===========================================================================
class Post < ActiveRecord::Base

  #=========================================================================
  # RELATIONS
  #=========================================================================
  belongs_to :user
  has_many :comments, dependent: :destroy
  has_many :likes, as: :likeable, dependent: :destroy

  #=========================================================================
  # VALIDATIONS
  #=========================================================================
  validates :title, :description, presence: true

end
